const FALLBACK = 'Сейчас AI-консультант временно недоступен. Попробуйте обновить страницу — иногда это помогает. Если проблема сохранится, опишите, что именно не получается, и передайте вопрос в поддержку.'

export async function POST(req: NextRequest) {
  const { messages, context } = await req.json() as {
    messages: Message[]
    context?: { page?: string; role?: string }
  }

  if (!Array.isArray(messages) || messages.length === 0) {
    return NextResponse.json({ reply: FALLBACK }, { status: 400 })
  }

  if (!process.env.OPENAI_API_KEY) {
    return NextResponse.json({ reply: FALLBACK })
  }

  const contextBlock = context
    ? `\n[КОНТЕКСТ: пользователь сейчас на странице «${context.page || 'неизвестно'}»${context.role ? `, роль: ${context.role}` : ''}]`
    : ''

  try {
    const response = await openai.chat.completions.create({
      model: 'gpt-4o-mini',
      max_tokens: 600,
      temperature: 0.5,
      messages: [
        { role: 'system', content: SYSTEM_PROMPT + contextBlock },
        ...messages,
      ],
    })
    const reply = response.choices[0].message.content?.trim() || FALLBACK
    return NextResponse.json({ reply })
  } catch {
    return NextResponse.json({ reply: FALLBACK })
  }
}
