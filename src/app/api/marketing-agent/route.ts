// ─── Helpers ──────────────────────────────────────────────────────────────────

function detectSalonType(services: string[]): string {
  const names = services.join(' ').toLowerCase()
  if (/маникюр|педикюр|гель.лак|ногт|наращ|покрыт/.test(names)) return 'ногтевой сервис'
  if (/брови|brow|оформл.*бров/.test(names)) return 'студия бровей'
  if (/ресниц|lash/.test(names)) return 'студия ресниц'
  if (/чистк.*лиц|пилинг|уход.*лиц|косметолог/.test(names)) return 'косметология'
  if (/стрижк|окрас|балаяж|укладк|волос/.test(names)) return 'парикмахерская'
  return 'салон красоты'
}

function currentSeason(): string {
  const m = new Date().getMonth()
  if (m >= 2 && m <= 4) return 'весна'
  if (m >= 5 && m <= 7) return 'лето'
  if (m >= 8 && m <= 10) return 'осень'
  return 'зима'
}

